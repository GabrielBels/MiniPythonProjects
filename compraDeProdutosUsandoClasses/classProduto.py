class Produto:
    def __init__(self, nome, preco):
        self.nome = nome
        self.preco = preco
   
    def setNome(self, nome):
        self.nome = nome
       
    def setPreco(self, preco):
        self.preco = preco
       
    def getNome():
        return self.nome
       
    def getPreco():
        return self.preco
